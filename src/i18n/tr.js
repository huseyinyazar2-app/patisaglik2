const dictionaries = { tr, en };

function normalizeLocale(locale) {
  return supportedLocales.some(item => item.code === locale) ? locale : 'tr';
}

export function getLocale() {
  try {
    const profile = JSON.parse(localStorage.getItem('pati_user_profile') || '{}');
    return normalizeLocale(profile.locale || 'tr');
  } catch {
    return 'tr';
  }
}

export function setLocale(locale) {
  const nextLocale = normalizeLocale(locale);
  try {
    const profile = JSON.parse(localStorage.getItem('pati_user_profile') || '{}');
    localStorage.setItem('pati_user_profile', JSON.stringify({ ...profile, locale: nextLocale }));
    document.documentElement.lang = nextLocale;
    document.documentElement.dir = supportedLocales.find(item => item.code === nextLocale)?.rtl ? 'rtl' : 'ltr';
  } catch {}
  return nextLocale;
}

function readValue(dictionary, key) {
  const keys = key.split('.');
  let val = dictionary;
  for (const k of keys) {
    if (val && typeof val === 'object' && k in val) {
      val = val[k];
    } else {
      return undefined;
    }
  }
  return val;
}

function interpolate(value, params = {}) {
  if (typeof value !== 'string') return value;
  return value.replace(/\{(\w+)\}/g, (_, name) => params[name] ?? `{${name}}`);
}

export function t(key, params = {}) {
  const locale = getLocale();
  return interpolate(readValue(dictionaries[locale], key) ?? readValue(en, key) ?? readValue(tr, key) ?? key, params);
}

export function translateForLocale(locale, key, params = {}) {
  const normalized = normalizeLocale(locale);
  return interpolate(readValue(dictionaries[normalized], key) ?? readValue(en, key) ?? readValue(tr, key) ?? key, params);
}

export default tr;
